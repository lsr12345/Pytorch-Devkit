import cv2
import numpy as np
from scipy import ndimage
from data.coco.coco_classes import COCO_CLASSES, COCO_LABEL, COCO_LABEL_MAP

def box_vis(img, cfg, boxes, scores, cls_ids):
    class_names = cfg.get('class_names', None)
    conf = cfg.get('test_conf', 0.1)

    if class_names is None:
        class_names = COCO_CLASSES

    if boxes is None:
        return img

    for i in range(len(boxes)):
        box = boxes[i]
        cls_id = int(cls_ids[i])
        score = scores[i]
        if score < conf:
            continue
        x0 = int(box[0])
        y0 = int(box[1])
        x1 = int(box[2])
        y1 = int(box[3])

        color = (_COLORS[cls_id] * 255).astype(np.uint8).tolist()
        text = '{}:{:.1f}%'.format(class_names[cls_id], score * 100)
        txt_color = (0, 0, 0) if np.mean(_COLORS[cls_id]) > 0.5 else (255, 255, 255)
        font = cv2.FONT_HERSHEY_SIMPLEX

        txt_size = cv2.getTextSize(text, font, 0.4, 1)[0]
        cv2.rectangle(img, (x0, y0), (x1, y1), color, 2)

        txt_bk_color = (_COLORS[cls_id] * 255 * 0.7).astype(np.uint8).tolist()
        cv2.rectangle(
            img,
            (x0, y0 + 1),
            (x0 + txt_size[0] + 1, y0 + int(1.5 * txt_size[1])),
            txt_bk_color,
            -1
        )
        cv2.putText(img, text, (x0, y0 + txt_size[1]), font, 0.4, txt_color, thickness=1)

    return img


def instance_vis(img, cfg, seg_label, cate_label, score, sort_by_density=False):
    score_thr = cfg.get('test_score_thr', 0.1)
    img_show = img.copy()
    h, w, _ = img.shape
    seg_label = seg_label[:, :h, :w]

    seg_label = seg_label.cpu().numpy().astype(np.uint8)
    # print(seg_label.sum())
    cate_label = cate_label.cpu().numpy()
    score = score.cpu().numpy()

    vis_inds = score > score_thr
    seg_label = seg_label[vis_inds]
    num_mask = seg_label.shape[0]
    cate_label = cate_label[vis_inds]
    cate_score = score[vis_inds]

    if sort_by_density:
        mask_density = []
        for idx in range(num_mask):
            cur_mask = seg_label[idx, :, :]
            cur_mask = cv2.resize(cur_mask, (w, h), interpolation= cv2.INTER_LINEAR)
            cur_mask = (cur_mask > 0.5).astype(np.int32)
            mask_density.append(cur_mask.sum())
        orders = np.argsort(mask_density)
        seg_label = seg_label[orders]
        cate_label = cate_label[orders]
        cate_score = cate_score[orders]

    np.random.seed(42)
    color_masks = [
        np.random.randint(0, 256, (1, 3), dtype=np.uint8)
        for _ in range(num_mask)
    ]
    #img_show = None
    font = cv2.FONT_HERSHEY_SIMPLEX
    for idx in range(num_mask):
        idx = -(idx+1)
        cur_mask = seg_label[idx, :, :]
        # cur_mask = cv2.resize(cur_mask, (w, h), interpolation= cv2.INTER_LINEAR)
        cur_mask = (cur_mask > 0.5).astype(np.uint8)
        if cur_mask.sum() == 0:
            # print('*')
            continue
        color_mask = color_masks[idx]
        cur_mask_bool = cur_mask.astype(np.bool)
        img_show[cur_mask_bool] = img[cur_mask_bool] * 0.5 + color_mask * 0.5

        #当前实例的类别
        cur_cate = cate_label[idx]      # 1-80
        realclass = COCO_LABEL[cur_cate]
        # realclass = COCO_LABEL[cur_cate-1]
        cur_score = cate_score[idx]

        name_idx = COCO_LABEL_MAP[realclass]
        label_text = COCO_CLASSES[name_idx-1]
        # label_text = COCO_CLASSES[name_idx]
        label_text += '|{:.02f}'.format(cur_score)
        center_y, center_x = ndimage.measurements.center_of_mass(cur_mask)
        vis_pos = (max(int(center_x) - 10, 0), int(center_y))
        cv2.putText(img_show, label_text, vis_pos, font, 0.4, (255, 255, 255))  # green
 
    return img_show

